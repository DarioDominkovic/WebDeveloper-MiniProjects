// generate a random hex color
function getRandomHexColor() {
  const letters = "0123456789ABCDEF";
  let color = "#";
  for (let i = 0; i < 6; i++) {
    color += letters[Math.floor(Math.random() * 16)];
  }
  return color;
}

// generate a random CSS color name
function getRandomCSSColorName() {
  return colorNames[Math.floor(Math.random() * colorNames.length)];
}

// change background color via hex color
hexButton.addEventListener("click", () => {
  const randomHexColor = getRandomHexColor();
  updateBackgroundColor(randomHexColor);
});

// change background color via color name
cssButton.addEventListener("click", () => {
  const randomCSSColorName = getRandomCSSColorName();
  updateBackgroundColor(randomCSSColorName);
});

// update background color and color info
function updateBackgroundColor(color) {
  document.body.style.backgroundColor = color;
  currentColor.textContent = `${color}`;
}
